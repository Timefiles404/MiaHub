package dev.timefiles.miaeco.model;

/**
 * 树的“建筑结构类型”，决定用哪个形态生成模型。
 * 与 CanopyShape 不同：CanopyShape 只描述树冠轮廓，TreeForm 决定整棵树的搭建方式。
 */
public enum TreeForm {
    /** 阔叶乔木：曲干（低位分导）+ 空壳裂片冠 + 辐条（橡树/红树/枫/银杏/樱）。 */
    BROADLEAF,
    /** 白桦/白杨：纤细白杆 + 层片小叶盘或紧凑高位小冠。 */
    BIRCH,
    /** 深色橡木：矮壮有机粗干 + 低垂浓密宽冠 + 夸张根盘。 */
    DARK_OAK,
    /** 热带雨林：高裸曲干 + 宽扁混叶伞冠 + 叶帘垂坠 + 藤蔓 + 板根。 */
    JUNGLE,
    /** 针叶：不规则短枝垫堆出的乱针尖塔；大个体为高耸巨杉（云杉）。 */
    CONIFER,
    /** 稀树草原伞形：强 S 曲细干低位分导 + 薄平顶叶盘（金合欢）。 */
    ACACIA,
    /** 垂柳：瘤结巨座 + 矮壮曲干 + 下瀑冠（叶帘垂近地）。 */
    WILLOW,
    /** 棕榈：单向倾倒弧细干（上段栅栏）+ 放射叶羽 + 顶簇。 */
    PALM,
    /** 榕树：S 曲干 + 极扁巨伞冠 + 冠底垂气根（部分扎地）。 */
    BANYAN,
    /** 灌木丛：低矮叶簇 + 台阶软化冠缘；高密度成片作小片林。 */
    SHRUB,
    /** 柏树尖塔：极窄竖椭壳 + 顶梢 + 表面草蕨羽饰。 */
    CYPRESS
}
